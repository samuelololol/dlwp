<div align="center"><font size="+3" style="font-family: sans-serif;"><b>News</b></font></div>
<p>
<ul>
<li><i>(9-Dec-2010)</i> <b>wxPython 2.8 Application Development Cookbook</b> by Cody Precord is published and <a href="#wxPyCookbook">available</a> for purchase.</li><p>

<li><i>(19-October-2010)</i> <b>wxPython 2.9.1.1</b> The first public release of wxPython from the 2.9 development <a href="http://wiki.wxpython.org/ReleaseSeries">release series</a>, with lots and lots (and lots) of changes, enhancements and fixes.  Details are in the <a href="recentchanges.php">Recent Changes</a> document.</li><p>

<li><i>(14-May-2010)</i> <b>wxPython 2.8.11.0</b> Adds Python 2.7 builds, PySlices, new pubsub implementation, lots of updates to AGW, and lots of bugs fixed.  </li><p>

<li><i>(22-May-2009)</i> The wx and wxPython mail lists are moving to Google Groups.  See the <a href="maillist.php">mail list page</a> for the new addresses and links.</li><p>

<li><i>(14-May-2009)</i> <b>wxPython 2.8.10.1</b> This release fixes the problem with using Python 2.6's default manifest, and updates wxcairo to work with the latest PyCairo.  </li><p>

<li><i>(16-Feb-2009)</i> <b>wxPython 2.8.9.2</b> This release adds the wx.lib.agw package, adds an event watcher to the widget inspection tool, and fixes a bunch of bugs.  </li><p>


<li><i>(8-Oct-2008)</i> A set of wxPython binaries for <b>Python 2.6</b> on the Mac and Windows (32-bit and 64-bit) are now available.</li><p>

<li><i>(29-Sept-2008)</i> <b>wxPython 2.8.9.1</b> is now <a href="download.php">available.</a> This release fixes a compatibility problem with Python 2.4.</li><p>

<li><i>(28-Sept-2008)</i> <b>wxPython 2.8.9.0</b> This release adds support for using Cairo for drawing on wx windows, adds a Win64 build, and various other fixes and enhancements. 
</li><p>

<li><i>(18-July-2008)</i> <b>wxPython 2.8.8.1</b> This is a minor bugfix release with several fixes for problems discovered in the 2.8.8.0 release.
<p>


<li><i>(23-June-2008)</i> <b>wxPython 2.8.8.0</b>  This release had a number of
further refinements and enhancements on the stable 2.8 source tree
since the previous release.   On Mac OS X 10.5 (Leopard) the Python
2.5 binaries of wxPython are able to be used with either Apple's
system Python, or with the Python.org version. <p>

